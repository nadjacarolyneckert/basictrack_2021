exam_grade = float(input("What is your exam grade?"))

if exam_grade >= 75:
    print(" Your grade is a First")

if 75 > exam_grade >= 70:
    print(" Your grade is a Upper Second")

if 70 > exam_grade >= 60:
    print(" Your grade is a Second")

if 60 > exam_grade >= 50:
    print(" Your grade is a Third")

if 50 > exam_grade >= 45:
    print(" Your grade is a F1 supp")

if 45 > exam_grade >= 40:
    print(" Your grade is a F2")

if 40> exam_grade:
    print(" Your grade is a F3")